          ['a', 2]=>" p ", ['b', 2]=>"|#p#", ['c', 2]=>"| p ", ['d', 2]=>"|#p#", ['e', 2]=>"| p ", ['f', 2]=>"|#p#", ['g', 2]=>"| p ", ['h', 2]=>"|#p#",
          ['a', 1]=>"#r#", ['b', 1]=>"| kn", ['c', 1]=>"|#b#", ['d', 1]=>"| Q ", ['e', 1]=>"|#K#", ['f', 1]=>"| b ", ['g', 1]=>"|#kn", ['h', 1]=>"| r "
        }
      )
    end
  end
end
